package javaSessions;

public class DoWhileLoopConcept {

	public static void main(String[] args) {
		
		int p =1;
		
		do {
			
			System.out.println(p);
			if(p==10) {
				break;
		}
			p++;
			
	}	
		
		while(p>=10);
		
		//use cases:
		//1. looking for a specific element on a page as soon as you launch
		

	}

}
